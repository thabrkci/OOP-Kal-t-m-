// Base class for living organisms
public class LivingOrganisms
{
    // Protected method for nutrition
    protected void Nutrition()
    {
        Console.WriteLine("Living organisms feed.");
    }

    // Protected method for respiration
    protected void Respiration()
    {
        Console.WriteLine("Living organisms respire.");
    }

    // Protected method for excretion
    protected void Excretion()
    {
        Console.WriteLine("Living organisms excrete.");
    }
}